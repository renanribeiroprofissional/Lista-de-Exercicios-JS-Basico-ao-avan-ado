// Crie um programa que leia um número e verifique se ele é positivo, negativo ou zero. Exiba a mensagem correspondente.

let numero = 0;

if (numero > 0) {
  console.log("Positivo");
} else if (numero < 0) {
  console.log("Negativo");
} else if (numero === 0) {
  console.log("Número zero");
} else {
  console.log("Obrigado...");
}
